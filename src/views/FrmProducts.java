    private void TblRegistrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TblRegistrosMouseClicked
        try {
            // TODO add your handling code here:
            int index = TblRegistros.getSelectedRow();
            TfID.setText(TblRegistros.getValueAt(index, 0).toString());
            TfNameProduct.setText(TblRegistros.getValueAt(index, 1).toString());
            TfPrice.setText(TblRegistros.getValueAt(index, 2).toString());
            String fechaSel = TblRegistros.getValueAt(index, 3).toString();
            Date fecha = new SimpleDateFormat("dd/MM/yyyy").parse(fechaSel);
            DCDueDate.setDate(fecha);
            CmbCategory.setSelectedItem(new Category(Integer.parseInt(TblRegistros.getValueAt(index, 5).toString())));
            BtnGuardar.setEnabled(false);
            BtnEditar.setEnabled(true);
            BtnBorrar.setEnabled(true);
        } catch (ParseException ex) {
            Logger.getLogger(FrmProducts.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_TblRegistrosMouseClicked

    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        // TODO add your handling code here:
        Date fecha = DCDueDate.getDate();
        System.out.println(fecha);
        Product prod = new Product(
                TfID.getText(),
                TfNameProduct.getText(),
                Double.parseDouble(TfPrice.getText()),
                fecha.toInstant().atZone(ZoneId.systemDefault()).toLocalDate(),
                (Category) CmbCategory.getSelectedItem()
        );
        int flag = daoProduct.addProduct(prod);
        if (flag == 1) {
            JOptionPane.showMessageDialog(this, "Registro guardado",
                    "Productos", JOptionPane.INFORMATION_MESSAGE);
            llenarTabla("");
            limpiar();
        }
    }//GEN-LAST:event_BtnGuardarActionPerformed

    private void BtnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBorrarActionPerformed
        // TODO add your handling code here:
        int resp = JOptionPane.showConfirmDialog(this, "Â¿Desea eliminar el registro?", "Productos",
                JOptionPane.YES_NO_OPTION);
        if (resp == 0) {
            int flag = daoProduct.deleteProducts(TfID.getText());
            if (flag == 1) {
                JOptionPane.showMessageDialog(this, "Registro eliminado satisfactoriamente.",
                        "Productos", JOptionPane.INFORMATION_MESSAGE);
                llenarTabla("");
                limpiar();
            }else{
                JOptionPane.showMessageDialog(this, "Error al eliminar...", "Productos", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BtnBorrarActionPerformed

